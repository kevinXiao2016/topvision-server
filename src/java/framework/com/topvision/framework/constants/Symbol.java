package com.topvision.framework.constants;

/**
 * 用来封装分隔符字符串。
 * 如果非公用分隔符，则由业务类自行建立私有常量
 * @author lzs
 * @created @2013-4-9-上午09:43:51
 *
 */
public class Symbol {

    /**
     * 斜线符号："/"
     */
    public static final String SLASH = "/";

    /**
     * 英文逗号：","
     */
    public static final String COMMA = ",";

    /**
     * 点，英文句号："."
     */
    public static final String POINT = ".";

    /**
     * 冒号：":"
     */
    public static final String COLON = ":";
    /**
     * 分号：";"
     */
    public static final String SEMICOLON = ";";
    /**
     * 等号："="
     */
    public static final String EQUAL = "=";
    /**
     * 百分号："%"
     */
    public static final String PERCENT = "%";

    /**
     * 下横线："_"
     */
    public static final String UNDERLINE = "_";

    /**
     * 左中括号："["
     */
    public static final String BRACKET_LEFT = "[";
    /**
     * 右中括号："]"
     */
    public static final String BRACKET_RIGHT = "]";
    /**
     * 左括弧："("
     */
    public static final String PARENTHESIS_LEFT = "(";
    /**
     * 右括弧：")"
     */
    public static final String PARENTHESIS_RIGHT = ")";
    /**
     * 左大括号："{"
     */
    public static final String BRACE_LEFT = "{";
    /**
     * 右大括号："}"
     */
    public static final String BRACE_RIGHT = "}";
    /**
     * 空字符串：""
     */
    public static final String EMPTY_STRING = "";
    /**
     * 中横线："-"
     */
    public static final String MIDDLE_LINE = "-";
}
